#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat <<'EOF'
Usage: scripts/no-dockerhub/start-stack.sh [--name <stack-name>] [--env-file <path>] [--rebuild] [--fresh]

Options:
  --name <stack-name>  Stack identifier used for container names/state dir (default: law-eye-local)
  --env-file <path>    Secrets env file (default: <user-state>/law-eye/no-dockerhub/<stack>/secrets.env)
  --rebuild            Rebuild local helper images (redis/postgres+pgvector)
  --fresh              Remove the postgres data volume before start (DANGEROUS: wipes data)
EOF
}

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"

STACK_NAME="${LAW_EYE_STACK_NAME:-law-eye-local}"
ENV_FILE=""
ENV_FILE_EXPLICIT=0
REBUILD_IMAGES=0
FRESH=0
RUNNING=0

while [[ $# -gt 0 ]]; do
  case "$1" in
    --name)
      STACK_NAME="${2:-}"
      shift 2
      ;;
    --env-file)
      ENV_FILE="${2:-}"
      ENV_FILE_EXPLICIT=1
      shift 2
      ;;
    --rebuild)
      REBUILD_IMAGES=1
      shift
      ;;
    --fresh)
      FRESH=1
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "ERROR: unknown argument: $1" >&2
      usage >&2
      exit 1
      ;;
  esac
done

if [[ -z "$STACK_NAME" ]]; then
  echo "ERROR: --name must not be empty" >&2
  exit 1
fi

DEFAULT_STATE_HOME="${XDG_STATE_HOME:-${HOME}/.local/state}"
DEFAULT_STATE_DIR="${DEFAULT_STATE_HOME}/law-eye/no-dockerhub/${STACK_NAME}"
LEGACY_STATE_DIR="$ROOT/tmp/no-dockerhub/$STACK_NAME"
STATE_DIR_RAW="${LAW_EYE_NO_DOCKERHUB_STATE_DIR:-$DEFAULT_STATE_DIR}"
if [[ -z "${LAW_EYE_NO_DOCKERHUB_STATE_DIR:-}" && -d "$LEGACY_STATE_DIR" ]]; then
  echo "SECURITY WARNING: legacy state dir exists under repo workspace: $LEGACY_STATE_DIR" >&2
  echo "  - This script will NOT use it anymore (to avoid secret sprawl)." >&2
fi
mkdir -p "$STATE_DIR_RAW"
STATE_DIR="$(cd "$STATE_DIR_RAW" && pwd)"
LOG_DIR="$STATE_DIR/logs"
PID_DIR="$STATE_DIR/pids"
mkdir -p "$LOG_DIR" "$PID_DIR"

if [[ -z "${LAW_EYE_NO_DOCKERHUB_STATE_DIR:-}" && -d "$LEGACY_STATE_DIR" ]]; then
  migrate_legacy_env_file() {
    local src="$1"
    local dst="$2"
    if [[ -f "$src" ]]; then
      echo "INFO: migrating legacy env file: ${src} -> ${dst}" >&2
      mkdir -p "$(dirname "$dst")"
      mv "$src" "$dst" 2>/dev/null || { cp "$src" "$dst" && rm -f "$src"; }
      chmod 600 "$dst" >/dev/null 2>&1 || true
    fi
  }

  if [[ ! -f "$STATE_DIR/secrets.env" ]]; then
    migrate_legacy_env_file "$LEGACY_STATE_DIR/.env" "$STATE_DIR/secrets.env"
    migrate_legacy_env_file "$LEGACY_STATE_DIR/.env.e2e" "$STATE_DIR/secrets.env"
  else
    migrate_legacy_env_file "$LEGACY_STATE_DIR/.env" "$STATE_DIR/legacy.repo.secrets.env"
    migrate_legacy_env_file "$LEGACY_STATE_DIR/.env.e2e" "$STATE_DIR/legacy.repo.secrets.e2e.env"
  fi
fi

cleanup_on_exit() {
  local exit_code=$?
  if [[ "$RUNNING" -eq 1 && "$exit_code" -ne 0 && "${LAW_EYE_KEEP_ON_ERROR:-0}" != "1" ]]; then
    echo "Startup failed (exit=$exit_code). Cleaning up stack: $STACK_NAME" >&2
    bash "$ROOT/scripts/no-dockerhub/stop-stack.sh" --name "$STACK_NAME" >/dev/null 2>&1 || true
  elif [[ "$RUNNING" -eq 1 && "$exit_code" -ne 0 && "${LAW_EYE_KEEP_ON_ERROR:-0}" == "1" ]]; then
    echo "Startup failed (exit=$exit_code). Keeping stack for debugging: $STACK_NAME" >&2
  fi
}
trap cleanup_on_exit EXIT

if [[ -f "$ROOT/.env" ]]; then
  echo "SECURITY WARNING: repo-root .env detected at $ROOT/.env" >&2
  echo "  - Gitignore is not a control. Do NOT store secrets in the repo workspace." >&2
  echo "  - This script will NOT read it; migrate secrets to your user state dir instead." >&2
fi

if [[ -z "$ENV_FILE" ]]; then
  ENV_FILE="$STATE_DIR/secrets.env"
fi

if [[ ! -f "$ENV_FILE" ]]; then
  if [[ "$ENV_FILE_EXPLICIT" -eq 1 ]]; then
    echo "ERROR: missing env file: $ENV_FILE" >&2
    exit 1
  fi

  echo "INFO: secrets env file not found; generating: $ENV_FILE" >&2
  if ! command -v python3 >/dev/null 2>&1; then
    echo "ERROR: missing required command: python3" >&2
    echo "Hint: Install Python 3 (used to generate local dev secrets)." >&2
    exit 1
  fi
  python3 - "$ENV_FILE" <<'PY'
import os
import secrets
import sys
from pathlib import Path

path = Path(sys.argv[1])
path.parent.mkdir(parents=True, exist_ok=True)

def token() -> str:
    return secrets.token_urlsafe(32)

content = "\n".join(
    [
        "# Auto-generated by scripts/no-dockerhub/start-stack.sh",
        "# DO NOT COMMIT. Keep this file outside the repo workspace.",
        f"POSTGRES_PASSWORD={token()}",
        f"REDIS_PASSWORD={token()}",
        "MINIO_ROOT_USER=minioadmin",
        f"MINIO_ROOT_PASSWORD={token()}",
        "",
    ]
)

path.write_text(content, encoding="utf-8")
PY
  chmod 600 "$ENV_FILE" >/dev/null 2>&1 || true
fi

if [[ ! -f "$ENV_FILE" ]]; then
  echo "ERROR: missing env file: $ENV_FILE" >&2
  exit 1
fi

set -a
# shellcheck disable=SC1090
source "$ENV_FILE"
set +a

if [[ -n "${POSTGRES_PASSWORD:-}" ]]; then
  POSTGRES_PASSWORD="$(printf '%s' "$POSTGRES_PASSWORD" | tr -d '\r')"
  export POSTGRES_PASSWORD
fi

if [[ -z "${POSTGRES_PASSWORD:-}" ]]; then
  echo "ERROR: POSTGRES_PASSWORD is empty (env file: $ENV_FILE)" >&2
  exit 1
fi

if [[ -n "${REDIS_PASSWORD:-}" ]]; then
  REDIS_PASSWORD="$(printf '%s' "$REDIS_PASSWORD" | tr -d '\r')"
  export REDIS_PASSWORD
fi

if [[ -z "${REDIS_PASSWORD:-}" ]]; then
  REDIS_PASSWORD="$(python3 - <<'PY'
import secrets
print(secrets.token_urlsafe(24))
PY
)"
  export REDIS_PASSWORD
fi

if [[ -n "${MINIO_ROOT_USER:-}" ]]; then
  MINIO_ROOT_USER="$(printf '%s' "$MINIO_ROOT_USER" | tr -d '\r')"
  export MINIO_ROOT_USER
fi

if [[ -z "${MINIO_ROOT_USER:-}" ]]; then
  MINIO_ROOT_USER="minioadmin"
  export MINIO_ROOT_USER
fi

if [[ -n "${MINIO_ROOT_PASSWORD:-}" ]]; then
  MINIO_ROOT_PASSWORD="$(printf '%s' "$MINIO_ROOT_PASSWORD" | tr -d '\r')"
  export MINIO_ROOT_PASSWORD
fi

if [[ -z "${MINIO_ROOT_PASSWORD:-}" ]]; then
  MINIO_ROOT_PASSWORD="$(python3 - <<'PY'
import secrets
print(secrets.token_urlsafe(24))
PY
)"
  export MINIO_ROOT_PASSWORD
fi

ensure_cmd() {
  local cmd="$1"
  local hint="$2"
  if ! command -v "$cmd" >/dev/null 2>&1; then
    echo "ERROR: missing required command: $cmd" >&2
    echo "Hint: $hint" >&2
    exit 1
  fi
}

ensure_cmd docker "Install Docker and ensure the daemon is running."
ensure_cmd python3 "Install Python 3 (used for port probing)."
ensure_cmd curl "Install curl (used for readiness checks)."
ensure_cmd pnpm "Install pnpm (used to start apps/web)."

# In non-login shells cargo may not be on PATH. Try sourcing rustup env if present.
if ! command -v cargo >/dev/null 2>&1; then
  if [[ -f "$HOME/.cargo/env" ]]; then
    # shellcheck disable=SC1090
    source "$HOME/.cargo/env"
  fi
fi
ensure_cmd cargo "Install Rust toolchain (rustup) and ensure cargo is on PATH."

WEB_RUNS_ON_WINDOWS=0
if [[ "${LAW_EYE_WEB_ON_WINDOWS:-}" == "1" ]]; then
  WEB_RUNS_ON_WINDOWS=1
elif [[ "${LAW_EYE_WEB_ON_WINDOWS:-}" == "0" ]]; then
  WEB_RUNS_ON_WINDOWS=0
else
  # Default: prefer running Web where pnpm is actually usable.
  #
  # On some Windows+WSL setups, pnpm exists on PATH as a WSL script that expects a Linux `node`,
  # but only Windows `node.exe` is installed. In that case, `pnpm` will fail inside WSL with
  # "exec: node: not found", so we must start the Next server on Windows instead.
  if pnpm -v >/dev/null 2>&1; then
    NODE_PLATFORM=""
    if command -v node >/dev/null 2>&1; then
      NODE_PLATFORM="$(node -p "process.platform" 2>/dev/null | tr -d '\r' | tr -d '\n' || true)"
    fi
    if [[ "$NODE_PLATFORM" == "win32" ]]; then
      WEB_RUNS_ON_WINDOWS=1
    else
      WEB_RUNS_ON_WINDOWS=0
    fi
  elif command -v cmd.exe >/dev/null 2>&1 && cmd.exe /c pnpm -v >/dev/null 2>&1; then
    WEB_RUNS_ON_WINDOWS=1
  else
    # Last resort: try detecting the active Node platform (Linux node vs Windows node.exe).
    NODE_PLATFORM=""
    if command -v node >/dev/null 2>&1; then
      NODE_PLATFORM="$(node -p "process.platform" 2>/dev/null | tr -d '\r' | tr -d '\n' || true)"
    elif command -v node.exe >/dev/null 2>&1; then
      NODE_PLATFORM="$(node.exe -p "process.platform" 2>/dev/null | tr -d '\r' | tr -d '\n' || true)"
    fi
    if [[ "$NODE_PLATFORM" == "win32" ]]; then
      WEB_RUNS_ON_WINDOWS=1
    fi
  fi
fi

WEB_MODE="${LAW_EYE_WEB_MODE:-dev}"
if [[ "$WEB_MODE" != "dev" && "$WEB_MODE" != "prod" ]]; then
  echo "ERROR: LAW_EYE_WEB_MODE must be 'dev' or 'prod' (got: $WEB_MODE)" >&2
  exit 1
fi

WEB_ENABLED=1
if [[ "${LAW_EYE_SKIP_WEB:-0}" == "1" ]]; then
  WEB_ENABLED=0
fi

get_primary_ipv4() {
  ip -4 addr show eth0 2>/dev/null | awk '/inet / {print $2}' | cut -d/ -f1 | head -n 1
}

get_windows_host_ip() {
  local gw=""
  gw="$(ip route 2>/dev/null | awk '/^default / {print $3; exit}' | tr -d '\r' | tr -d '\n' || true)"
  if [[ -n "$gw" ]]; then
    echo "$gw"
    return 0
  fi
  awk '/^nameserver / {print $2; exit}' /etc/resolv.conf 2>/dev/null | tr -d '\r' | tr -d '\n' || true
}

to_windows_path() {
  local path="$1"

  if command -v wslpath >/dev/null 2>&1; then
    wslpath -w "$path" | tr -d '\r'
    return 0
  fi

  if [[ "$path" =~ ^/mnt/([a-zA-Z])/(.*)$ ]]; then
    local drive="${BASH_REMATCH[1]}"
    local rest="${BASH_REMATCH[2]//\//\\}"
    printf '%s:\\%s' "${drive^^}" "$rest"
    return 0
  fi

  echo "$path"
}

port_free_wsl() {
  local port="$1"
  python3 - "$port" <<'PY'
import socket, sys
port = int(sys.argv[1])
s = socket.socket()
try:
    s.bind(("0.0.0.0", port))
except OSError:
    sys.exit(1)
finally:
    s.close()
sys.exit(0)
PY
}

port_free_windows() {
  local port="$1"

  if command -v cmd.exe >/dev/null 2>&1; then
    cmd.exe /c "netstat -ano | findstr /R /C\":$port .*LISTENING\" >nul" >/dev/null 2>&1
    if [[ $? -eq 0 ]]; then
      return 1
    fi
    return 0
  fi

  if command -v powershell.exe >/dev/null 2>&1; then
    powershell.exe -NoProfile -Command "try { if (Get-NetTCPConnection -State Listen -LocalPort $port -ErrorAction Stop) { exit 1 } else { exit 0 } } catch { exit 0 }" >/dev/null 2>&1
    return $?
  fi

  return 0
}

port_free() {
  local port="$1"
  port_free_wsl "$port" && port_free_windows "$port"
}

choose_port_wsl() {
  local name="$1"
  shift
  for candidate in "$@"; do
    if port_free_wsl "$candidate"; then
      echo "$candidate"
      return 0
    fi
  done
  echo "ERROR: no free WSL port candidates for $name ($*)" >&2
  return 1
}

choose_port_range_wsl() {
  local name="$1"
  local start="$2"
  local end="$3"

  local candidate
  for ((candidate = start; candidate <= end; candidate++)); do
    if port_free_wsl "$candidate"; then
      echo "$candidate"
      return 0
    fi
  done

  echo "ERROR: no free WSL port in range for $name ($start..$end)" >&2
  return 1
}

choose_port_windows() {
  local name="$1"
  shift
  for candidate in "$@"; do
    if port_free_windows "$candidate"; then
      echo "$candidate"
      return 0
    fi
  done
  echo "ERROR: no free Windows port candidates for $name ($*)" >&2
  return 1
}

choose_port_range_windows() {
  local name="$1"
  local start="$2"
  local end="$3"

  local candidate
  for ((candidate = start; candidate <= end; candidate++)); do
    if port_free_windows "$candidate"; then
      echo "$candidate"
      return 0
    fi
  done

  echo "ERROR: no free Windows port in range for $name ($start..$end)" >&2
  return 1
}

choose_port() {
  local name="$1"
  shift
  for candidate in "$@"; do
    if port_free "$candidate"; then
      echo "$candidate"
      return 0
    fi
  done
  echo "ERROR: no free port candidates for $name ($*)" >&2
  return 1
}

choose_port_range() {
  local name="$1"
  local start="$2"
  local end="$3"

  local candidate
  for ((candidate = start; candidate <= end; candidate++)); do
    if port_free "$candidate"; then
      echo "$candidate"
      return 0
    fi
  done

  echo "ERROR: no free port in range for $name ($start..$end)" >&2
  return 1
}

if [[ -n "${POSTGRES_PORT:-}" ]] && ! port_free_wsl "${POSTGRES_PORT}"; then
  echo "WARN: POSTGRES_PORT=${POSTGRES_PORT} is not available; auto-selecting a free port." >&2
  unset POSTGRES_PORT
fi
if [[ -n "${REDIS_PORT:-}" ]] && ! port_free_wsl "${REDIS_PORT}"; then
  echo "WARN: REDIS_PORT=${REDIS_PORT} is not available; auto-selecting a free port." >&2
  unset REDIS_PORT
fi
if [[ -n "${API_PORT:-}" ]] && ! port_free_wsl "${API_PORT}"; then
  echo "WARN: API_PORT=${API_PORT} is not available; auto-selecting a free port." >&2
  unset API_PORT
fi
if [[ -n "${WEB_PORT:-}" ]]; then
  if [[ "$WEB_RUNS_ON_WINDOWS" -eq 1 ]] && ! port_free_windows "${WEB_PORT}"; then
    echo "WARN: WEB_PORT=${WEB_PORT} is not available on Windows; auto-selecting a free port." >&2
    unset WEB_PORT
  elif [[ "$WEB_RUNS_ON_WINDOWS" -eq 0 ]] && ! port_free_wsl "${WEB_PORT}"; then
    echo "WARN: WEB_PORT=${WEB_PORT} is not available in WSL; auto-selecting a free port." >&2
    unset WEB_PORT
  fi
fi
if [[ -n "${MINIO_API_PORT:-}" ]] && ! port_free_wsl "${MINIO_API_PORT}"; then
  echo "WARN: MINIO_API_PORT=${MINIO_API_PORT} is not available; auto-selecting a free port." >&2
  unset MINIO_API_PORT
fi
if [[ -n "${MINIO_CONSOLE_PORT:-}" ]] && ! port_free_wsl "${MINIO_CONSOLE_PORT}"; then
  echo "WARN: MINIO_CONSOLE_PORT=${MINIO_CONSOLE_PORT} is not available; auto-selecting a free port." >&2
  unset MINIO_CONSOLE_PORT
fi

POSTGRES_PORT="${POSTGRES_PORT:-$(choose_port_range_wsl postgres 15435 15550 2>/dev/null || choose_port_wsl postgres 5435 5436)}"
REDIS_PORT="${REDIS_PORT:-$(choose_port_range_wsl redis 16380 16450 2>/dev/null || choose_port_wsl redis 6380 6381)}"
API_PORT="${API_PORT:-$(choose_port_range_wsl api 13000 13150 2>/dev/null || choose_port_wsl api 3001 3003 3005)}"
WEB_PORT="${WEB_PORT:-$(
  if [[ "$WEB_RUNS_ON_WINDOWS" -eq 1 ]]; then
    choose_port_range_windows web 18849 18950 2>/dev/null || choose_port_windows web 8849 8850 8851
  else
    choose_port_range_wsl web 18849 18950 2>/dev/null || choose_port_wsl web 8849 8850 8851
  fi
)}"
MINIO_API_PORT="${MINIO_API_PORT:-$(choose_port_range_wsl minio-api 19000 19100 2>/dev/null || choose_port_wsl minio-api 9000)}"
MINIO_CONSOLE_PORT="${MINIO_CONSOLE_PORT:-$(choose_port_range_wsl minio-console 19101 19200 2>/dev/null || choose_port_wsl minio-console 9001)}"

if ! port_free_wsl "$POSTGRES_PORT"; then
  echo "ERROR: POSTGRES_PORT=$POSTGRES_PORT is not available" >&2
  exit 1
fi
if ! port_free_wsl "$REDIS_PORT"; then
  echo "ERROR: REDIS_PORT=$REDIS_PORT is not available" >&2
  exit 1
fi
if ! port_free_wsl "$API_PORT"; then
  echo "ERROR: API_PORT=$API_PORT is not available" >&2
  exit 1
fi
if [[ "$WEB_RUNS_ON_WINDOWS" -eq 1 ]] && ! port_free_windows "$WEB_PORT"; then
  echo "ERROR: WEB_PORT=$WEB_PORT is not available on Windows" >&2
  exit 1
fi
if [[ "$WEB_RUNS_ON_WINDOWS" -eq 0 ]] && ! port_free_wsl "$WEB_PORT"; then
  echo "ERROR: WEB_PORT=$WEB_PORT is not available in WSL" >&2
  exit 1
fi
if ! port_free_wsl "$MINIO_API_PORT"; then
  echo "ERROR: MINIO_API_PORT=$MINIO_API_PORT is not available" >&2
  exit 1
fi
if ! port_free_wsl "$MINIO_CONSOLE_PORT"; then
  echo "ERROR: MINIO_CONSOLE_PORT=$MINIO_CONSOLE_PORT is not available" >&2
  exit 1
fi

echo "Stack: $STACK_NAME"
echo "Using ports: postgres=$POSTGRES_PORT redis=$REDIS_PORT minio=$MINIO_API_PORT api=$API_PORT web=$WEB_PORT"

image_exists() {
  docker image inspect "$1" >/dev/null 2>&1
}

build_redis_image() {
  docker build -t lawsaw-redis:local - <<'EOF'
FROM mcr.microsoft.com/devcontainers/base:ubuntu@sha256:3dcb059253b2ebb44de3936620e1cff3dadcd2c1c982d579081ca8128c1eb319
ARG DEBIAN_FRONTEND=noninteractive
RUN apt-get update \
  && apt-get install -y redis-server \
  && rm -rf /var/lib/apt/lists/*
WORKDIR /data
USER 1000:1000
EXPOSE 6379
CMD ["sh", "-c", "test -n \"$REDIS_PASSWORD\" || { echo \"REDIS_PASSWORD is required\" >&2; exit 1; }; exec redis-server --appendonly yes --protected-mode yes --bind 0.0.0.0 --port 6379 --requirepass \"$REDIS_PASSWORD\""]
EOF
}

build_postgres_image() {
  docker build -t lawsaw-postgres-pgvector:local - <<'EOF'
FROM mcr.microsoft.com/devcontainers/base:ubuntu@sha256:3dcb059253b2ebb44de3936620e1cff3dadcd2c1c982d579081ca8128c1eb319
ARG DEBIAN_FRONTEND=noninteractive

RUN apt-get update \
  && apt-get install -y --no-install-recommends ca-certificates curl gnupg \
  && rm -rf /var/lib/apt/lists/*

# Install PostgreSQL 16 + pgvector from PGDG.
RUN set -eux; \
  echo "deb [signed-by=/usr/share/keyrings/postgresql.gpg] http://apt.postgresql.org/pub/repos/apt noble-pgdg main" > /etc/apt/sources.list.d/pgdg.list; \
  curl -fsSL https://www.postgresql.org/media/keys/ACCC4CF8.asc | gpg --dearmor -o /usr/share/keyrings/postgresql.gpg; \
  apt-get update; \
  apt-get install -y --no-install-recommends postgresql-16 postgresql-contrib-16 postgresql-16-pgvector; \
  rm -rf /var/lib/apt/lists/*

ENV PGDATA=/var/lib/postgresql/data
RUN mkdir -p /var/lib/postgresql/data \
  && chown -R postgres:postgres /var/lib/postgresql \
  && chmod 700 /var/lib/postgresql/data

RUN cat > /usr/local/bin/docker-entrypoint.sh <<'EOS'
#!/usr/bin/env bash
set -euo pipefail

PGDATA="${PGDATA:-/var/lib/postgresql/data}"
POSTGRES_USER="${POSTGRES_USER:-postgres}"
POSTGRES_PASSWORD="${POSTGRES_PASSWORD:-}"
POSTGRES_DB="${POSTGRES_DB:-postgres}"

BIN="/usr/lib/postgresql/16/bin"

if [[ -z "${POSTGRES_PASSWORD}" ]]; then
  echo "ERROR: POSTGRES_PASSWORD is required" >&2
  exit 1
fi
if [[ "${POSTGRES_PASSWORD}" == "postgres" || "${POSTGRES_PASSWORD}" == "password" ]]; then
  echo "ERROR: POSTGRES_PASSWORD must not be a default value" >&2
  exit 1
fi
if [[ -z "${POSTGRES_USER}" ]]; then
  echo "ERROR: POSTGRES_USER is required" >&2
  exit 1
fi
if [[ -z "${POSTGRES_DB}" ]]; then
  echo "ERROR: POSTGRES_DB is required" >&2
  exit 1
fi

if [ ! -s "${PGDATA}/PG_VERSION" ]; then
  mkdir -p "${PGDATA}"
  chmod 700 "${PGDATA}"
  "${BIN}/initdb" -D "${PGDATA}" --encoding=UTF8 --locale=C

  echo "listen_addresses='*'" >> "${PGDATA}/postgresql.conf"
  echo "password_encryption='scram-sha-256'" >> "${PGDATA}/postgresql.conf"
  echo "host all all 0.0.0.0/0 scram-sha-256" >> "${PGDATA}/pg_hba.conf"
  echo "host all all ::/0 scram-sha-256" >> "${PGDATA}/pg_hba.conf"

  "${BIN}/pg_ctl" -D "${PGDATA}" -o "-c listen_addresses='*'" -w start

  psql -v ON_ERROR_STOP=1 --username=postgres -v app_user="${POSTGRES_USER}" -v app_pass="${POSTGRES_PASSWORD}" <<'SQL'
SELECT format(
  'CREATE ROLE %I WITH LOGIN PASSWORD %L NOSUPERUSER NOCREATEDB NOCREATEROLE NOREPLICATION BYPASSRLS',
  :'app_user',
  :'app_pass'
) WHERE NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = :'app_user');
\gexec

SELECT format(
  'ALTER ROLE %I WITH LOGIN PASSWORD %L NOSUPERUSER NOCREATEDB NOCREATEROLE NOREPLICATION BYPASSRLS',
  :'app_user',
  :'app_pass'
) WHERE EXISTS (SELECT 1 FROM pg_roles WHERE rolname = :'app_user');
\gexec
SQL

  psql -v ON_ERROR_STOP=1 --username=postgres -v app_user="${POSTGRES_USER}" <<'SQL'
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'law_eye_app') THEN
        CREATE ROLE law_eye_app NOLOGIN NOSUPERUSER NOBYPASSRLS;
    END IF;
END $$;

ALTER ROLE law_eye_app NOLOGIN NOSUPERUSER NOBYPASSRLS;

SELECT format('GRANT law_eye_app TO %I', :'app_user');
\gexec
SQL

  psql -v ON_ERROR_STOP=1 --username=postgres -v db_name="${POSTGRES_DB}" -v db_owner="${POSTGRES_USER}" <<'SQL'
SELECT format('CREATE DATABASE %I OWNER %I', :'db_name', :'db_owner')
WHERE NOT EXISTS (SELECT 1 FROM pg_database WHERE datname = :'db_name');
\gexec

SELECT format('ALTER DATABASE %I OWNER TO %I', :'db_name', :'db_owner')
WHERE EXISTS (SELECT 1 FROM pg_database WHERE datname = :'db_name');
\gexec
SQL

  psql -v ON_ERROR_STOP=1 --username=postgres --dbname="${POSTGRES_DB}" -c 'CREATE EXTENSION IF NOT EXISTS "uuid-ossp";'
  psql -v ON_ERROR_STOP=1 --username=postgres --dbname="${POSTGRES_DB}" -c 'CREATE EXTENSION IF NOT EXISTS pgcrypto;'
