start_bg() {
  local name="$1"
  local workdir="$2"
  local pid_file="$PID_DIR/$name.pid"
  local log_file="$LOG_DIR/$name.log"
  shift 2

  if [[ -f "$pid_file" ]]; then
    local existing_pid
    existing_pid="$(cat "$pid_file" 2>/dev/null || true)"
    if [[ -n "$existing_pid" ]] && pid_exists "$existing_pid"; then
      # API can be alive with a stale pid file but bound to a different port
      # from a previous stack run. Treat that as stale and restart it.
      if [[ "$name" == "api" ]]; then
        local existing_pid_raw="${existing_pid#wsl:}"
        local listening_pid
        listening_pid="$(wsl_listen_pid "$API_PORT")"
        if [[ -n "$listening_pid" && "$listening_pid" == "$existing_pid_raw" ]]; then
          echo "Process already running: $name (pid=$existing_pid)"
          return 0
        fi
        echo "WARN: stale api pid file detected (pid=$existing_pid, api_port=$API_PORT, listening_pid=${listening_pid:-none}); restarting api." >&2
        rm -f "$pid_file"
      else
        echo "Process already running: $name (pid=$existing_pid)"
        return 0
      fi
    fi
  fi

  echo "Starting $name..."
  (
    cd "$workdir"
    nohup "$@" >"$log_file" 2>&1 &
    echo "wsl:$!" >"$pid_file"
  )
}

pid_exists() {
  local pid="$1"
  pid="$(echo "$pid" | tr -d '\r' | tr -d '\n')"
  if [[ -z "$pid" ]]; then
    return 1
  fi

  if [[ "$pid" == wsl:* ]]; then
    kill -0 "${pid#wsl:}" >/dev/null 2>&1
    return $?
  fi

  if [[ "$pid" == win:* ]]; then
    local win_pid="${pid#win:}"
    if [[ -z "$win_pid" ]]; then
      return 1
    fi
    if command -v tasklist.exe >/dev/null 2>&1; then
      tasklist.exe /FI "PID eq $win_pid" 2>/dev/null | tr -d '\r' | grep -q " $win_pid " && return 0
    fi
    return 1
  fi

  # Safety default: treat unknown format as WSL PID.
  kill -0 "$pid" >/dev/null 2>&1
}

wsl_listen_pid() {
  local port="$1"
  ss -ltnp 2>/dev/null \
    | awk -v p=":${port}" '$4 ~ p { print }' \
    | sed -n 's/.*pid=\([0-9]\+\).*/\1/p' \
    | head -n 1
}

windows_listen_pid() {
  local port="$1"
  if ! command -v powershell.exe >/dev/null 2>&1; then
    return 1
  fi
  powershell.exe -NoProfile -Command "Get-NetTCPConnection -State Listen -LocalPort $port -ErrorAction SilentlyContinue | Select-Object -First 1 -ExpandProperty OwningProcess" 2>/dev/null | tr -d '\r' | tr -d '\n' || true
}

