wait_for_api_health() {
  local attempts="${1:-90}"
  for _ in $(seq 1 "$attempts"); do
    if curl -fsS "http://localhost:${API_PORT}/health" >/dev/null 2>&1; then
      return 0
    fi
    sleep 1
  done
  curl -fsS "http://localhost:${API_PORT}/health" >/dev/null 2>&1
}

